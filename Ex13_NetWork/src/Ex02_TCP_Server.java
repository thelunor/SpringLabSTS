import java.io.DataOutputStream;
import java.io.IOException;
import java.io.OutputStream;
import java.net.ServerSocket;
import java.net.Socket;

//TCP 서버
//server(process) - client(process)

//서버 : 192.168.0.24
//포트 : port :9999

public class Ex02_TCP_Server {
	public static void main(String[] args) throws IOException {
		ServerSocket serversocket = new ServerSocket(9999); // parameter는 포트번호
		System.out.println("접속 대기중 ...");
		Socket socket = serversocket.accept(); //클라이언트가 접속하면 소켓끼리 연결
		System.out.println("연결완료");
		
		//연결이되면 
		//서버 : 클라이언트 (read , write)
		//socket 과 socket 
		//socket(inputStream , outputStream) 
		OutputStream out = socket.getOutputStream();
		DataOutputStream dos = new DataOutputStream(out); //8가지 기본 타입 + 알파
		dos.writeUTF("문자데이터 Byte 통신 가능"); // writeUTF: 2byte 단위의 문자통신
		
		System.out.println("서버 종료");
		
		dos.close();
		out.close();
		socket.close();
		serversocket.close();
	}

}



